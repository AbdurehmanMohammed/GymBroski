/** Substring search on exercise names for inline autocomplete (e.g. "bic" → Bicycle Crunch). */
export function filterExercisesByNameQuery(query, limit = 12) {
  const q = String(query ?? '').trim().toLowerCase();
  if (!q) return [];
  return EXERCISE_LIBRARY.filter(
    (e) => typeof e?.name === 'string' && e.name.toLowerCase().includes(q)
  ).slice(0, limit);
}
